#programa que cuenta de 1 a 10
#ciclo while
contador = 1    # 1. inicializar la variable
while contador <= 10: # 2. crear una acondición lógica
    print(contador)
    contador = contador + 1 # 3. hay que variar a la variable
    
#ciclo for
for contador in range(1,11):
    print(contador)

